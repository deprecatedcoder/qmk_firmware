// Set power down behavior
void suspend_power_down_user(void) {
    backlight_disable();
    rgblight_disable_noeeprom();
}


// Set wake up behavior
void suspend_wakeup_init_user(void) {
    backlight_enable();
    breathing_disable();
    backlight_level_noeeprom(bl_level);
    rgblight_enable_noeeprom();
}


// Per-key TAPPING_TERM
uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case TD(TD_SHRUG):
            return TAPPING_TERM + 20;
        case TD(TD_LAYER):
            return 130;
        default:
            return TAPPING_TERM;
    }
}


// Custom key handling
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case BL_FAST:
            // Decrease the breathing period (breathe faster)
            breathing_period_dec();
            break;
        case BL_SLOW:
            // Increase the breathing period (breathe slower)
            breathing_period_inc();
            break;
        case BL_DEC:
        case BL_INC:
            // Store current backlight level
            bl_level = get_backlight_level();
            break;
        case BL_BRTG:
            // Store current backlight breathing state
            bl_breathing = is_breathing();
            break;
        case BOARDY:
            if (record->event.pressed) {
                // Link to Boardy's keymap
                SEND_STRING("https://github.com/deprecatedcoder/qmk_firmware/blob/master/keyboards/org60/keymaps/boardy/keymap.c");
            }
            break;
        case S_ALLYOU:
            if (record->event.pressed) {
                // Salt: All you...
                tap_code(KC_Y);
                SEND_STRING("All you...");
                tap_code(KC_ENT);
            }
            break;
        case S_DC:
            if (record->event.pressed) {
                // Salt: Dead center ;-)
                tap_code(KC_T);
                SEND_STRING("Dead center ;-)");
                tap_code(KC_ENT);
            }
            break;
        case S_DISTRACT:
            if (record->event.pressed) {
                // Salt: Does anyone else...
                tap_code(KC_T);
                SEND_STRING("Does anyone else find it really distracting when someone sende a really long message right at faceoff?");
                tap_code(KC_ENT);
            }
            break;
        case S_OOF:
            if (record->event.pressed) {
                // Salt: Oof...
                tap_code(KC_T);
                SEND_STRING("Oof...");
                tap_code(KC_ENT);
            }
            break;
        case S_YOUGOT:
            if (record->event.pressed) {
                // Salt: You got this...
                tap_code(KC_Y);
                SEND_STRING("You got this...");
                tap_code(KC_ENT);
            }
            break;
        case WINCMPS:
            if (record->event.pressed) {
                // Output WinCompose URL
                SEND_STRING("https://github.com/samhocevar/wincompose/releases");
            }
            break;
        }

    return true;
};


// Actions on layer state change
layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
       case _BASE:
       case _ITALICS:
       case _SALT:
            // Toggle breathing to match state
            if (is_breathing() != bl_breathing) breathing_toggle();

            // Set backlight to stored level
            if (get_backlight_level() != bl_level) backlight_level_noeeprom(bl_level);

            // Set underglow to snake-mode (counterclockwise)
            if (rgblight_get_mode() != RGBLIGHT_MODE_SNAKE+1) rgblight_mode_noeeprom(RGBLIGHT_MODE_SNAKE+1);

            break;
        case _FUNCTION:
            // Toggle breathing to match state
            if (is_breathing() != bl_breathing) breathing_toggle();

            // Set backlight to double stored level
            if (get_backlight_level() != bl_level*2) backlight_level_noeeprom(bl_level*2);

            // Set underglow to breathing-mode (fast)
            if (rgblight_get_mode() != RGBLIGHT_MODE_BREATHING+3) {
                rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING+3);
                rgblight_set_speed_noeeprom(255);
            }

            break;
        case _LIGHTING:
            // Disable breathing
            if (is_breathing()) breathing_disable();

            // Set backlight to max
            if (get_backlight_level() != BACKLIGHT_LEVELS) backlight_level_noeeprom(BACKLIGHT_LEVELS);

            break;
        case _ADVFUNC:
            // Enable breathing
            if (!is_breathing()) breathing_enable();

            // Set the breathing period to fast
            if (get_breathing_period() != 1) breathing_period_set(1);

            // Set backlight to max
            if(get_backlight_level() != BACKLIGHT_LEVELS) backlight_level_noeeprom(BACKLIGHT_LEVELS);

            break;
        default:
            break;
    }

    return state;
};


// Tap Dance Definitions
qk_tap_dance_action_t tap_dance_actions[] = {
    [TD_LAYER] = ACTION_TAP_DANCE_FN_ADVANCED (NULL, layers_dance_finished, layers_dance_reset),
    [TD_SHRUG] = ACTION_TAP_DANCE_FN_ADVANCED (NULL, shrug_dance_finished, shrug_dance_reset),
};


/**
 *  Layer Tap Dance functions
 *
 *  - Tap or hold: toggle _FUNCTION layer and return to _BASE
 */
void layers_dance_finished(qk_tap_dance_state_t *state, void *user_data) {
    td_state = cur_dance(state);

    switch(td_state) {
        case SINGLE_TAP:
        case SINGLE_HOLD:
            layer_clear();
            layer_on(_FUNCTION);
            break;
        default:
            break;
    }
}
void layers_dance_reset(qk_tap_dance_state_t *state, void *user_data) {
    switch(td_state) {
        case SINGLE_TAP:
        case SINGLE_HOLD:
            layer_off(_FUNCTION);
            break;
        default:
            break;
    }
}


/**
 *  ¯\_(ツ)_/¯ Tap Dance functions
 *
 *  - Tap: OneShot SHIFT
 *  - Hold: SHIFT
 *  - 2+: ¯\_(ツ)_/¯
 */
void shrug_dance_finished(qk_tap_dance_state_t *state, void *user_data) {
    td_state = cur_dance(state);

    switch(td_state) {
        case SINGLE_TAP:
            set_oneshot_mods(MOD_LSFT);
            break;
        case SINGLE_HOLD:
            register_code (KC_LSFT);
            break;
        case DOUBLE_TAP:
            break;
        case A_LOT_OF_TAPS:
            // ¯\_(ツ)_/¯
            send_unicode_hex_string("00AF 005C 005F 0028 30C4 0029 005F 002F 00AF");
            break;
        default:
            break;
    }
}
void shrug_dance_reset(qk_tap_dance_state_t *state, void *user_data) {
    switch (td_state) {
        case SINGLE_TAP:
            clear_oneshot_mods();
            break;
        case SINGLE_HOLD:
            unregister_code (KC_LSFT);
            break;
        default:
            break;
    }
}


// Set the Tap Dance state
int cur_dance (qk_tap_dance_state_t *state) {
    if (state->count == 1) {
        if (!state->pressed) return SINGLE_TAP;
        else return SINGLE_HOLD;
    } else if (state->count == 2) {
        return DOUBLE_TAP;
    } else {
        return A_LOT_OF_TAPS;
    }
}
